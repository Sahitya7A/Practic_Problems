//C++ program to demonstrate operators
#include<iostream>
using namespace std;
int main()
{
    int a,b,c;
    cout<<"enter a,b values"<<endl;
    cin>>a>>b;
    c=a+b;
    cout<<"Addition : "<<c<<endl;
    c=a-b;
    cout<<"Subtraction :"<<c<<endl;
    c=a*b;
    cout<<"Multiplication :"<<c<<endl;
    c=a/b;
    cout<<"Division :"<<c<<endl;
    c=a%b;
    cout<<"Modulo :"<<c<<endl;
    return 0;
}
